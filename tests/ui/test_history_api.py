@pytest.mark.remote
def test_history_multiple_stations_sections(client: TestClient) -> None:
    """Test that requesting history for multiple stations returns 200 and correct data."""
    response = client.get(
        "/api/history",
        params={
            "provider": "dwd",
            "network": "observation",
            "parameters": "daily/kl",
            "station": "01048",
            "sections": "name,device,missing_data",
            "with_metadata": "true",
            "with_stations": "true",
        },
    )
    assert response.status_code == 200
    data = response.json()
    assert list(data.keys()) == ["metadata", "stations", "histories"]
    first = data["histories"][0]
    assert set(first.keys()) == {"name", "device", "missing_data"}

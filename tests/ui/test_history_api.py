"""Tests for the /api/history endpoint."""

import pytest
from dirty_equals import IsDatetime
from fastapi.testclient import TestClient


@pytest.fixture
def client() -> TestClient:
    """Test client for the FastAPI app."""
    from wetterdienst.ui.restapi import app  # noqa: PLC0415

    return TestClient(app)


def test_history_no_station_returns_422(client: TestClient) -> None:
    """Test that requesting history without station or all parameter returns 422."""
    response = client.get(
        "/api/history",
        params={
            "provider": "dwd",
            "network": "observation",
            "parameters": "daily/kl",
        },
    )
    assert response.status_code == 400
    # pydantic model validation error
    detail = response.json()["detail"]
    assert detail == "Either 'station' or 'all' parameter must be provided to query history."


